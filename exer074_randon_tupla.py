# programa para armazenar numeors aleatorios em tuplas.
from random import randint
nr=(randint(1,100),randint(1,50),randint(1,60),randint(1,40),randint(1,30),randint(1,16),randint(1,68))
print(f"{nr}")# linha para imprimir a tupla por extenso.
print(f" o maio numero é {max(nr)}")
print(f"o menor numero é {min(nr)}")

